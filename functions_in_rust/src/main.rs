fn main() {
   my_function(11,22);
}
fn my_function (x:i32, y:i32){
    println!("The value of x is {}",x);
    println!("The value of y is {}",y);
    println!("The value of x+y is {}",x+y); 
    println!("The value of x-y is {}",x-y);
    println!("The value of x*y is {}",x*y);
    println!("The value of x/y is {}",x/y);
    println!("The value of x%y is {}",x%y);
    println!("The value of x^y is {}",x^y);
    println!("The value of x&y is {}",x&y);
    println!("The value of x|y is {}",x|y);
   
}
